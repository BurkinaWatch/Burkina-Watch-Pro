// Statistiques des boutiques
export function getBoutiquesStats() {
  const total = BOUTIQUES_DATA.length;
  const avecClimatisation = BOUTIQUES_DATA.filter(b => b.climatisation).length;
  const avecLivraison = BOUTIQUES_DATA.filter(b => b.livraison).length;
  const avecParking = BOUTIQUES_DATA.filter(b => b.parking).length;
  
  const parCategorie: Record<string, number> = {};
  const parRegion: Record<string, number> = {};
  const parVille: Record<string, number> = {};
  
  BOUTIQUES_DATA.forEach(b => {
    parCategorie[b.categorie] = (parCategorie[b.categorie] || 0) + 1;
    parRegion[b.region] = (parRegion[b.region] || 0) + 1;
    parVille[b.ville] = (parVille[b.ville] || 0) + 1;
  });
  
  return {
    total,
    avecClimatisation,
    avecLivraison,
    avecParking,
    parCategorie,
    parRegion,
    nombreVilles: Object.keys(parVille).length
  };
}
