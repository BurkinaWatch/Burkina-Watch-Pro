// Statistiques des marchés
export function getMarchesStats() {
  const total = MARCHES_DATA.length;
  
  const parType: Record<string, number> = {};
  const parRegion: Record<string, number> = {};
  const parVille: Record<string, number> = {};
  
  let totalCommercants = 0;
  
  MARCHES_DATA.forEach(m => {
    parType[m.type] = (parType[m.type] || 0) + 1;
    parRegion[m.region] = (parRegion[m.region] || 0) + 1;
    parVille[m.ville] = (parVille[m.ville] || 0) + 1;
    if (m.nombreCommerçants) {
      totalCommercants += m.nombreCommerçants;
    }
  });
  
  return {
    total,
    totalCommercants,
    parType,
    parRegion,
    nombreVilles: Object.keys(parVille).length
  };
}
